#include "libft.h"

void	ft_putnbr(long long n)
{
	int fl;

	fl = 1;
	if (n < 0)
	{
		ft_putchar('-');
		fl = -1;
	}
	if (n / 10)
		ft_putnbr(n / 10 * fl);
	ft_putchar(n % 10 * fl + 48);
}

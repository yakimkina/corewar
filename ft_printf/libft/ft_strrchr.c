char	*ft_strrchr(const char *s, int c)
{
	char *p;

	p = 0;
	while (*s != '\0')
	{
		if (*s == (char)c)
			p = (char *)s;
		s++;
	}
	if (*s == (char)c)
		p = (char *)s;
	return (p);
}
